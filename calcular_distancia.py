""" Se desea calcular la distancia recirrida (m) por un m√≥vil que tiene una velocidad constante (m/s) durante un tiempo T (Sg), considerar que es un movimiento rectilineo uniforme """

""" 
Distancia recorrida (m) = D
Velocidad constante (m/s) = V
Tiempo (sg) = T
"""

v = int(input("Ingrese la velocidad (m/s): "))
t = int(input("Ingrese el tiempo (sg): "))
d = v * t

print("La distancia recorrida es: ", d, "m")
